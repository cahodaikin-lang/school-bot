def has_mat(text: str) -> bool:
    if not text:
        return False
    clean = text.lower().replace("*", "").replace("0", "–æ").replace("3", "–µ").replace("—ë", "–µ").replace(" ", "")
    return any(word in clean for word in MAT_WORDS)

